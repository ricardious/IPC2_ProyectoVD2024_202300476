class User:
    def __init__(
        self, user_id, pwd, full_name, email, phone_number, address, profile_url
    ):
        self.user_id = user_id
        self.pwd = pwd
        self.full_name = full_name
        self.email = email
        self.phone_number = phone_number
        self.address = address
        self.profile_url = profile_url
